#include <iostream>
using namespace std;

int main() {
    int a, b;
    a = 10;
    b = 5;

    // print the sum of a and b
    cout << "a + b = " << (a + b) << endl;

    // print the substraction of a and b
    cout << "a - b = " << (a - b) << endl;

    // print the product of a and b
    cout << "a * b = " << (a * b) << endl;

    // print the division of a by b
    cout << "a / b = " << (a / b) << endl;

    // print the modulo of a by b
    cout << "a % b = " << (a % b) << endl;

    return 0;
}